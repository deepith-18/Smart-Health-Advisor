import streamlit as st

st.title("About the Smart Health Advisor")

st.markdown("""
This application is a demonstration of how Generative AI can be used to create helpful, interactive tools. It is powered by **Google's Gemini model** and built with the **Streamlit** framework.

### **Purpose**
The goal is to provide users with a quick, AI-driven analysis of their health symptoms. The AI is designed to give structured and cautious advice, always emphasizing the importance of consulting with a real healthcare professional.

### **Technology Stack**
-   **Frontend:** Streamlit
-   **AI Engine:** Python, Google Generative AI (Gemini 1.5 Flash)
-   **Text-to-Speech:** Google Text-to-Speech (gTTS)

### **Important Disclaimer**
This is a proof-of-concept project and **NOT a medical device**. The information provided is for educational purposes only and should never replace a consultation with a qualified doctor.
""")